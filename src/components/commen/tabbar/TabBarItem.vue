<template>
  <div class="tab-bar-item" @click="itemClick">
    <div v-if="!isActive"><slot name="item-icon"></slot></div>
    <div v-else><slot name="item-active"></slot></div>
    <div :style="activeStyle"><slot name="item-name"></slot></div>
  </div>
</template>

<script>
export default {
  name:'TabBarItem',
  props: {
    path: String,
    aciveColor:{
      type:String,
      default: 'red',
    }
  },
  data() {
    return {
      
    }
  },
  computed:{
    isActive() {
      //判断字符串
      return this.$route.path.includes(this.path)
    },
    activeStyle() {
      return this.isActive ? {color: this.aciveColor} : {}
    }
  },
  methods: {
    itemClick() {
      this.$router.replace(this.path).catch(err=>{})
    }
  },
}
</script>

